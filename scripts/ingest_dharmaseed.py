async def _require_author(session: AsyncSession, slug: str) -> Author:
    author = (
        await session.execute(sa.select(Author).where(Author.slug == slug))
    ).scalar_one_or_none()
    if author is None:
        raise LookupError(
            f"Author with slug={slug!r} not seeded — add a migration first "
            "(see alembic/versions/20260509_007_dharmaseed_source_type.py)."
        )
    return author


async def _load_talk(
    session: AsyncSession,
    *,
    record: TalkRecord,
    teacher_author: Author,
    consent_ledger_ref: str,
    license_str: str,
) -> tuple[bool, int, int]:
    """Insert one talk's FRBR + chunks. Returns (skipped, n_parents, n_children).

    Idempotent via ``content_hash`` on Instance — same body hash → skip.
    """
    existing = (
        await session.execute(
            sa.select(Instance).where(Instance.content_hash == record.body_hash).limit(1)
        )
    ).scalar_one_or_none()
    if existing is not None:
        return True, 0, 0

    work = await _get_or_create_work(session, record=record)
    expression = await _get_or_create_expression(
        session,
        work_id=work.id,
        author_id=teacher_author.id,
        record=record,
        license_str=license_str,
        consent_ledger_ref=consent_ledger_ref,
    )

    instance = Instance(
        expression_id=expression.id,
        source_url=f"https://dharmaseed.org/talks/{record.dharmaseed_id}/",
        source_format="whisperai-large-v3-text",
        retrieved_at=datetime.now(UTC),
        content_hash=record.body_hash,
        storage_path=str(record.file_path),
        metadata_json={
            "dharmaseed_id": record.dharmaseed_id,
            "talks_json_meta": record.talk_meta,
            "transcription_engine": "WhisperAI Pro (Whisper large-v3)",
        },
    )
    session.add(instance)
    await session.flush()

    parents = chunk_talk(record.body)
    teacher_name = teacher_author.name
    n_parents = 0
    n_children = 0
    next_sequence = 0
    for parent in parents:
        parent_row = Chunk(
            instance_id=instance.id,
            sequence=next_sequence,
            text=parent.text,
            text_ascii_fold=to_ascii_fold(parent.text),
            token_count=parent.token_count,
            is_parent=True,
            segment_id=None,  # talks have no segment IDs
            metadata_json={
                "stage": "parent",
                "position": parent.position,
                "child_count": len(parent.children),
            },
        )
        session.add(parent_row)
        await session.flush()
        n_parents += 1
        next_sequence += 1

        for child in parent.children:
            session.add(
                Chunk(
                    instance_id=instance.id,
                    parent_chunk_id=parent_row.id,
                    sequence=next_sequence,
                    text=child.text,
                    text_ascii_fold=to_ascii_fold(child.text),
                    token_count=child.token_count,
                    is_parent=False,
                    segment_id=None,
                    context_text=_build_context_text(
                        title=record.title,
                        teacher_name=teacher_name,
                        date_iso=record.date_iso,
                        dharmaseed_id=record.dharmaseed_id,
                    ),
                    context_version=_CONTEXT_VERSION,
                    context_model=_CONTEXT_MODEL,
                    metadata_json={
                        "stage": "child",
                        "position_in_parent": child.position_in_parent,
                    },
                )
            )
            n_children += 1
            next_sequence += 1
    await session.flush()
    return False, n_parents, n_children


async def _get_or_create_work(session: AsyncSession, *, record: TalkRecord) -> Work:
    """Look up a Work by canonical_id, creating it on first ingest."""
    work = (
        await session.execute(sa.select(Work).where(Work.canonical_id == record.canonical_id))
    ).scalar_one_or_none()
    if work is not None:
        return work
    work = Work(
        canonical_id=record.canonical_id,
        title=record.title,
        title_pali=None,
        tradition_code="pragmatic_dharma",
        primary_language_code="eng",
        source_type="dharmaseed_talk",
        is_restricted=False,
        metadata_json={
            "dharmaseed_id": record.dharmaseed_id,
            "date": record.date_iso,
        },
    )
    session.add(work)
    await session.flush()
    return work


async def _get_or_create_expression(
    session: AsyncSession,
    *,
    work_id: UUID,
    author_id: UUID,
    record: TalkRecord,
    license_str: str,
    consent_ledger_ref: str,
) -> Expression:
    """Look up by (work_id, author_id, language) — Burbea has one EN per talk."""
    existing = (
        await session.execute(
            sa.select(Expression).where(
                Expression.work_id == work_id,
                Expression.author_id == author_id,
                Expression.language_code == "eng",
            )
        )
    ).scalar_one_or_none()
    if existing is not None:
        return existing
    expression = Expression(
        work_id=work_id,
        author_id=author_id,
        language_code="eng",
        title=record.title,
        publication_year=int(record.date_iso[:4]) if record.date_iso[:4].isdigit() else None,
        edition_note="WhisperAI large-v3 transcription",
        license=license_str,
        consent_ledger_ref=consent_ledger_ref,
        metadata_json={"date": record.date_iso},
    )
    session.add(expression)
    await session.flush()
    return expression


