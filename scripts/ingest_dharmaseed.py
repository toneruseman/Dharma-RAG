_HEADER_END_RE = re.compile(r"^TRANSCRIPTION:\s*$", re.MULTILINE)
_FOOTER_START_RE = re.compile(r"^Generated by WhisperAI\s*$", re.MULTILINE)
_FILENAME_RE = re.compile(r"^([a-z]+)_(\d+)_(.+)\.txt$")


@dataclass(slots=True)
class TalkRecord:
    """Parsed transcript + matching metadata, ready for FRBR insert."""

    canonical_id: str  # e.g. "rb_12345"
    dharmaseed_id: str  # raw id from talks.json
    title: str
    date_iso: str  # YYYY-MM-DD or empty if unknown
    body: str  # transcript body, header/footer stripped
    body_hash: str  # sha256 of cleaned body — stable identity
    file_path: Path  # original .txt for storage_path provenance
    talk_meta: dict[str, Any]  # raw entry from talks.json (for archival)


@dataclass(slots=True)
class IngestStats:
    talks_seen: int = 0
    talks_inserted: int = 0
    talks_skipped: int = 0
    talks_errored: int = 0
    parents_inserted: int = 0
    children_inserted: int = 0


# ---------------------------------------------------------------------------
# Parsing
# ---------------------------------------------------------------------------


def _strip_transcript(raw: str) -> str:
    """Drop WhisperAI report header + footer, return prose body only."""
    header_match = _HEADER_END_RE.search(raw)
    if header_match is None:
        # No "TRANSCRIPTION:" line — return everything except trailing
        # generator boilerplate. Defensive — handles non-standard
        # outputs from older WhisperAI runs.
        body = raw
    else:
        body = raw[header_match.end() :]
    footer_match = _FOOTER_START_RE.search(body)
    if footer_match is not None:
        body = body[: footer_match.start()]
    return body.strip()


def _parse_filename(filename: str) -> tuple[str, str, str] | None:
    """Extract (teacher_slug, dharmaseed_id, title_slug) from a filename.

    Burbea files look like ``rb_10024_emotions_and_freedom.txt``;
    Sucitto files like ``as_10231_opening_the_door.txt``. Returns
    None when the filename does not match the convention so the
    caller can either skip or surface a warning.
    """
    m = _FILENAME_RE.match(filename)
    if m is None:
        return None
    return m.group(1), m.group(2), m.group(3)


def _build_talks_index(talks_json_path: Path) -> dict[str, dict[str, Any]]:
    """Parse the per-teacher talks.json into ``id → entry`` lookup.

    `id` is the dharmaseed talk-id (string). When an entry's `name`
    field disagrees with the file naming convention we still match by
    id — newer transcripts sometimes have the canonical filename ahead
    of a metadata refresh.
    """
    raw = json.loads(talks_json_path.read_text(encoding="utf-8"))
    return {entry["id"]: entry for entry in raw if "id" in entry}


def _build_talk_record(
    *,
    txt_path: Path,
    talks_index: dict[str, dict[str, Any]],
    teacher_slug: str,
) -> TalkRecord | None:
    """Parse a single transcript .txt + match its metadata.

    The filename prefix is a Dharmaseed convention (e.g. ``rb_`` for
    Burbea, ``as_`` for Sucitto) and need not match the author slug
    in ``author_t``. We accept any ``<prefix>_<id>_<title>.txt`` form
    and rely on the per-teacher ``talks.json`` (constructed from the
    same source) to provide authoritative metadata.
    """
    parsed = _parse_filename(txt_path.name)
    if parsed is None:
        logger.warning("Skipping unrecognised filename: %s", txt_path.name)
        return None
    _file_prefix, dharmaseed_id, title_slug = parsed

    raw = txt_path.read_text(encoding="utf-8", errors="replace")
    body = _strip_transcript(raw)
    if not body or len(body) < 200:
        logger.warning("Empty / short body in %s (%d chars)", txt_path.name, len(body))
        return None

    meta = talks_index.get(dharmaseed_id, {})
    title = meta.get("title") or _humanise_slug(title_slug)
    date_iso = meta.get("date") or ""

    body_hash = hashlib.sha256(body.encode("utf-8")).hexdigest()

    canonical_id = f"{teacher_slug}_{dharmaseed_id}"

    return TalkRecord(
        canonical_id=canonical_id,
        dharmaseed_id=dharmaseed_id,
        title=title,
        date_iso=date_iso,
        body=body,
        body_hash=body_hash,
        file_path=txt_path,
        talk_meta=meta,
    )


def _humanise_slug(slug: str) -> str:
    """Best-effort title from a filename slug (fallback when json-meta missing).

    ``opening_the_door`` → ``Opening the Door``. Stop-words stay
    lowercase per minimal title-casing — a one-liner, not a proper
    English title-caser, but readable enough for the ~3 talks where
    metadata is missing (see concept-37 §«461 vs 458»).
    """
    words = slug.replace("_", " ").split()
    if not words:
        return slug
    return " ".join(w.capitalize() if i == 0 else _smart_case(w) for i, w in enumerate(words))


_LOWER_TITLE_WORDS = frozenset({"a", "an", "the", "of", "in", "on", "and", "or", "to", "for"})


def _smart_case(word: str) -> str:
    return word if word.lower() in _LOWER_TITLE_WORDS else word.capitalize()


