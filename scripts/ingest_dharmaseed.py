"""Ingest Dharmaseed transcripts into the Dharma-RAG corpus (rag-day-37).

Pilot: Rob Burbea (461 talks at ``Q:\\dharmaseed\\transcripts\\rob_burbea\\``).
Variant A from concept-37 — single ``dharma_v2`` Qdrant collection,
``Work.source_type='dharmaseed_talk'`` payload tag drives the
per-corpus filter on retrieval. Deterministic context prefix (no LLM
round-trip) — see concept-37 §4.4 for rationale.

Usage::

    python scripts/ingest_dharmaseed.py \\
        --teacher-slug rob_burbea \\
        --transcripts-dir Q:/dharmaseed/transcripts/rob_burbea \\
        --talks-json Q:/dharmaseed/rob_burbea_talks.json

Idempotent: re-running on the same transcripts produces zero new
inserts (content_hash match → skip). Adding a new talk later? Same
command — only the new file gets inserted.

License/consent:
* Default `Expression.license = 'CC-BY-NC-ND-4.0'`
* `Expression.consent_ledger_ref = 'local-dev/dharmaseed-{teacher}-pilot.yaml'`
  → marker that this is local-dev only; a public-launch ingest must
  flip the ref to a populated ledger entry. See concept-37 §«Лицензии».
"""

