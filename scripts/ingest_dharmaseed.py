# Deterministic context prefix
# ---------------------------------------------------------------------------


def _build_context_text(
    *,
    title: str,
    teacher_name: str,
    date_iso: str,
    dharmaseed_id: str,
) -> str:
    """Single-sentence narrative stamp prepended to each child chunk.

    Deterministic — no LLM. Carries the four anchor facts BGE-M3 needs
    to disambiguate teacher / topic / time across thousands of similar
    insight-tradition talks.

    Empty or unknown values are quietly elided rather than producing
    awkward placeholders ("titled ''"). This keeps the prefix clean
    when a transcript has no matching json metadata.
    """
    parts: list[str] = ["Excerpt from a recorded dharma talk"]
    if title:
        parts.append(f'titled "{title}"')
    if teacher_name:
        parts.append(f"by {teacher_name}")
    if date_iso:
        parts.append(f"recorded {date_iso}")
    sentence = ", ".join(parts) + "."
    if dharmaseed_id:
        sentence += f" Source: dharmaseed.org/talks/{dharmaseed_id}."
    return sentence


_CONTEXT_VERSION = "deterministic-v1"
_CONTEXT_MODEL = "metadata-prefix-v1"


