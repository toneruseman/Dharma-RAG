async def _run(args: argparse.Namespace) -> int:
    logging.basicConfig(level=logging.INFO, format="%(asctime)s %(levelname)s %(message)s")
    settings = get_settings()

    transcripts_dir = Path(args.transcripts_dir)
    talks_json_path = Path(args.talks_json)
    if not transcripts_dir.is_dir():
        print(f"ERROR: transcripts dir not found: {transcripts_dir}", file=sys.stderr)
        return 1
    if not talks_json_path.is_file():
        print(f"ERROR: talks json not found: {talks_json_path}", file=sys.stderr)
        return 1

    talks_index = _build_talks_index(talks_json_path)
    print(f"Loaded {len(talks_index)} talk-metadata entries from {talks_json_path.name}")

    txt_files = sorted(transcripts_dir.glob("*.txt"))
    print(f"Found {len(txt_files)} .txt files in {transcripts_dir}")

    engine = create_async_engine(settings.database_url, future=True, echo=False)
    session_maker = async_sessionmaker(engine, expire_on_commit=False)
    stats = IngestStats()
    start = time.monotonic()
    try:
        async with session_maker() as session:
            teacher_author = await _require_author(session, args.teacher_slug)
            consent_ledger_ref = f"local-dev/dharmaseed-{args.teacher_slug}-pilot.yaml"
            for i, txt_path in enumerate(txt_files, start=1):
                stats.talks_seen += 1
                try:
                    record = _build_talk_record(
                        txt_path=txt_path,
                        talks_index=talks_index,
                        teacher_slug=args.teacher_slug,
                    )
                    if record is None:
                        stats.talks_skipped += 1
                        continue
                    skipped, n_p, n_c = await _load_talk(
                        session,
                        record=record,
                        teacher_author=teacher_author,
                        consent_ledger_ref=consent_ledger_ref,
                        license_str=args.license,
                    )
                    if skipped:
                        stats.talks_skipped += 1
                    else:
                        stats.talks_inserted += 1
                        stats.parents_inserted += n_p
                        stats.children_inserted += n_c
                except Exception as exc:  # noqa: BLE001
                    stats.talks_errored += 1
                    logger.exception("Failed on %s: %s", txt_path.name, exc)
                if i % args.commit_every == 0:
                    await session.commit()
                    elapsed = time.monotonic() - start
                    print(
                        f"[{i}/{len(txt_files)}] commit OK — "
                        f"inserted={stats.talks_inserted} "
                        f"skipped={stats.talks_skipped} "
                        f"errors={stats.talks_errored} "
                        f"parents={stats.parents_inserted} "
                        f"children={stats.children_inserted} "
                        f"elapsed={elapsed:.1f}s"
                    )
            await session.commit()
    finally:
        await engine.dispose()

    elapsed = time.monotonic() - start
    print()
    print("=== rag-day-37 dharmaseed ingest finished ===")
    print(f"  teacher:           {args.teacher_slug}")
    print(f"  txt files seen:    {stats.talks_seen}")
    print(f"  talks inserted:    {stats.talks_inserted}")
    print(f"  talks skipped:     {stats.talks_skipped}")
    print(f"  talks errored:     {stats.talks_errored}")
    print(f"  parents inserted:  {stats.parents_inserted}")
    print(f"  children inserted: {stats.children_inserted}")
    print(f"  wallclock:         {elapsed:.1f}s")
    if stats.talks_errored > 0:
        print()
        print("Some talks errored — check log above. Re-run is idempotent.")
        return 2
    return 0


def _parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Ingest Dharmaseed transcripts into Dharma-RAG")
    p.add_argument("--teacher-slug", required=True, help="Author slug (e.g. rob_burbea)")
    p.add_argument(
        "--transcripts-dir",
        required=True,
        help="Directory of .txt transcripts (e.g. Q:/dharmaseed/transcripts/rob_burbea)",
    )
    p.add_argument(
        "--talks-json",
        required=True,
        help="Per-teacher metadata json (e.g. Q:/dharmaseed/rob_burbea_talks.json)",
    )
    p.add_argument(
        "--license",
        default="CC-BY-NC-ND-4.0",
        help="License stamp on Expression rows. Default Dharmaseed canonical.",
    )
    p.add_argument(
        "--commit-every",
        type=int,
        default=20,
        help="Commit every N talks to bound rollback cost. Default 20.",
    )
    return p.parse_args()


if __name__ == "__main__":
    raise SystemExit(asyncio.run(_run(_parse_args())))
